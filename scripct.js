function alterarImagem(objeto, caminhoNovaImagem){
		document.getElementById(objeto).src = caminhoNovaImagem; 
        /*document.getElementById: acessa um objeto na página através do ID
		src: é abreviação de source, é o caminho da nova imagem.
		= caminhoNovaImagem: Informa o diretório da nova imagem
		*/
}		